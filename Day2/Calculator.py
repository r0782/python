Number1=float(input("Enter a number:"))
Operator=input("Entre a operation which you want to do:")
Number2=float(input("Enter a number:"))
if Operator == "+":
    print("Answer is :",Number1+Number2)
elif Operator == "-":
     print("Answer is :",Number1-Number2)
elif Operator == "*":
     print("Answer is :",Number1*Number2)
elif Operator == "%":
     print("Answer is :",Number1%Number2)
elif Operator == "/":
     print("Answer is :",Number1/Number2)
elif Operator == "**":
     print("Answer is :",Number1**Number2)